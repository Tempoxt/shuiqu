import java.awt.Color;

/**生成模板水印文字*/
public class ShareTemplateTextModel {
	/**水印文字*/
	String textContent;
	/**水印的字体颜色*/
	Color fontColor;
	/**水印的字体样式*/
	Integer fontStyle;
	/**水印的字体大小*/
	Integer fontSize;
	/**水印的字体名称, 如黑体*/
	String fontName;
	/**水印文字透明度*/
	float alpha;
	/**打印起始X像素*/
	Integer fontX;
	/**打印起始Y像素*/
	Integer fontY;	
	
	public String getTextContent() {
		return textContent;
	}
	public void setTextContent(String textContent) {
		this.textContent = textContent;
	}
	public Color getFontColor() {
		return fontColor;
	}
	public void setFontColor(Color fontColor) {
		this.fontColor = fontColor;
	}
	public Integer getFontStyle() {
		return fontStyle;
	}
	public void setFontStyle(Integer fontStyle) {
		this.fontStyle = fontStyle;
	}
	public Integer getFontSize() {
		return fontSize;
	}
	public void setFontSize(Integer fontSize) {
		this.fontSize = fontSize;
	}
	public String getFontName() {
		return fontName;
	}
	public void setFontName(String fontName) {
		this.fontName = fontName;
	}
	public float getAlpha() {
		return alpha;
	}
	public void setAlpha(float alpha) {
		this.alpha = alpha;
	}
	public Integer getFontX() {
		return fontX;
	}
	public void setFontX(Integer fontX) {
		this.fontX = fontX;
	}
	public Integer getFontY() {
		return fontY;
	}
	public void setFontY(Integer fontY) {
		this.fontY = fontY;
	}
	@Override
	public String toString() {
		return "ShareTemplateTextModel [textContent=" + textContent + ", fontColor=" + fontColor + ", fontStyle=" + fontStyle + ", fontSize=" + fontSize + ", fontName=" + fontName + ", alpha=" + alpha + ", fontX=" + fontX + ", fontY=" + fontY + ", getTextContent()=" + getTextContent() + ", getFontColor()=" + getFontColor() + ", getFontStyle()=" + getFontStyle() + ", getFontSize()=" + getFontSize() + ", getFontName()=" + getFontName() + ", getAlpha()=" + getAlpha() + ", getFontX()=" + getFontX() + ", getFontY()=" + getFontY() + ", getClass()=" + getClass() + ", hashCode()=" + hashCode() + ", toString()=" + super.toString() + "]";
	}
	
	
	
}
